const search_query = document.getElementById('search_query');
const query_count = document.getElementById('query-count');

const debounce = (funct, time) => {
	let timeoutId;
	return function () {
		if (timeoutId) {
			clearTimeout(timeoutId)
		}
		const context = this
		const args = arguments
		timeoutId = setTimeout(() => {
			funct.apply(context, args)
		}, time);
	}
}

search_query.addEventListener("keyup", (e) => {
	renderResultDebounce(e.target.value)
})

const renderResult = (query) => {

	console.log(query)

	const res = iconscout.filter(fa => {
		const temp = fa;

		let match = true;

		query.split(' ').forEach(q => {
			if (q && q.length)
				match = match && temp.match(`.*${q}.*`)
		})

		if (match) {
			return fa
		}
	}).map(fa => {

		const inner = `<li class="dib ma0 pb3 w-grid-2 w-grid-4-m w-grid-6-l w-grid-9-xl">
		  <div class="flex flex-column justify-center relative shadow-hover default hover-bg-white br2 hide-child">
		    <a class="flex flex-column items-center justify-center color-inherit w-100 pa2 br2 br--top no-underline hover-bg-blue4 hover-white">
		      <i class="uil uil-${fa}"></i>
		    </a>
		    <div class="w-100 ph1 pv2 tc f2">
		      <span class="db gray5 hover-gray7 text select-all">${fa}</span>
		    </div>
		  </div>
		</li>`

		return inner

	})

	query_count.innerText = res.length == iconscout.length ? `All ${res.length}` : `${res.length} Resulting`
	faElem.innerHTML = res.join('');
}

const renderResultDebounce = debounce(renderResult, 500)

